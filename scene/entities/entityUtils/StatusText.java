package scene.entities.entityUtils;

public class StatusText {
	
	public String msg;
	public int priority;
	public float r, g, b;
	
	public StatusText(String msg, int priority, float r, float g, float b) {
		this.msg = msg;
		this.priority = priority;
		this.r = r;
		this.g = g;
		this.b = b;
	}

}
